# Arithmetic operations
a = 10
b = 3

sum = a + b       # Addition
diff = a - b      # Subtraction
prod = a * b      # Multiplication
quot = a / b      # Division
remainder = a % b # Modulus

print(sum, diff, prod, quot, remainder)

# Comparison operations
is_equal = (a == b)
is_greater = (a > b)
is_less = (a < b)

print(is_equal, is_greater, is_less)
