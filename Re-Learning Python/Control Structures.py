# If-else statement
x = 10
if x > 5:
    print("x is greater than 5")
    print("___________________")
else:
    print("x is less than or equal to 5")

# For loop
for i in range(5):
    print(i)
print("___________________")

# While loop
i = 0
while i < 5:
    print(i)
    i += 1
print("___________________")

