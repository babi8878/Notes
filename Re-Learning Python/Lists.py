# Create a list
fruits = ["apple", "banana", "cherry"]

# Access list items
print(fruits[0])  # Output: apple

# Modify list items
fruits[1] = "blueberry"
print(fruits)  # Output: ['apple', 'blueberry', 'cherry']

# Add items to the list
fruits.append("orange")
print(fruits)  # Output: ['apple', 'blueberry', 'cherry', 'orange']

# Loop through a list
for fruit in fruits:
    print(fruit)
